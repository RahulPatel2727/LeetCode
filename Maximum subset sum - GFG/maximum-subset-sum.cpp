#include<bits/stdc++.h>
using namespace std;


class IntArray
{
public:
    template <class T>
    static void input(vector<T> &A,int n)
    {
        for (int i = 0; i < n; i++)
        {
            scanf("%d ",&A[i]);
        }
    }

    template <class T>
    static void print(vector<T> &A)
    {
        for (int i = 0; i < A.size(); i++)
        {
            cout << A[i] << " ";
        }
        cout << endl;
    }
};


// } Driver Code Ends
class Solution {

  public:
    long long findMaxSubsetSum(int N, vector<int> &arr) {
        vector<pair<long long, long long>>ans(N+1);
        for(int i=1;i<=N;i++) ans[i]={max(ans[i-1].first, ans[i-1].second)+arr[i-1], ans[i-1].first};
        return max(ans.back().first, ans.back().second);
    }
};


